def get_ixl_link(subtopic):
    """
    Get the IXL link for a given subtopic.
    
    Args:
        subtopic (str): The subtopic name as it appears in subjects_config.py
        
    Returns:
        str: The IXL URL if it exists, None otherwise
    """
    return IXL_ALGEBRA1_LINKS.get(subtopic)


def get_all_mapped_subtopics():
    """
    Get a list of all subtopics that have IXL links mapped.
    
    Returns:
        list: List of subtopic names that have IXL links
    """
    return list(IXL_ALGEBRA1_LINKS.keys())


def get_unmapped_subtopics():
    """
    Get subtopics from subjects_config.py that don't have IXL links yet.
    
    Returns:
        list: List of subtopics without IXL links
    """
    from subjects_config import SUBJECTS_AND_SUBTOPICS
    
    all_algebra1_subtopics = SUBJECTS_AND_SUBTOPICS.get("Algebra 1", [])
    mapped_subtopics = set(IXL_ALGEBRA1_LINKS.keys())
    
    return [subtopic for subtopic in all_algebra1_subtopics if subtopic not in mapped_subtopics]
